package medium

import (
	"fmt"
	"testing"
	"time"
)

func Test_isValidSudoku(t *testing.T) {
	board := [][]byte{
		{'5','3','.', '.','7','.', '.','.','.'},
		{'6','.','.', '1','9','5', '.','.','.'},
		{'.','9','8', '.','.','.', '.','6','.'},
		{'8','.','.', '.','6','.', '.','.','3'},
		{'4','.','.', '8','.','3', '.','.','1'},
		{'7','.','.', '.','2','.', '.','.','6'},
		{'.','6','.', '.','.','.', '2','8','.'},
		{'.','.','.', '4','1','9', '.','.','5'},
		{'.','.','.', '.','8','.', '.','7','9'},
	}
	got := isValidSudoku(board)
	fmt.Println(fmt.Sprintf("isValidSudoku result is %v", got))
	time.Sleep(1 * time.Second)
}



